// js introduction

// js can change html content
// one of many js html methods is getElementById()
// the example below finds an html element with id="demo" and changes the element content
// (innerHTML) to "Hello JavaScript"

// example
document.getElementById("demo").innerHTML = "Hello JavaScript";

// js accepts both double and single quotes

// example
document.getElementById("demo").innerHTML = "Hello JavaScript";

// js can change html styles (css)
// changing the style of an html element, is a variant of changing an html attribute

// example
document.getElementById("demo").style.fontSize = "35px";

// js can hide html elements
// hiding html elements can be done by changing the display style

// example
document.getElementById("demo").style.display = "none";

// js can show html elements
// showing hidden html elements can also be done by changing the display style
document.getElementById("demo").style.display = "block";
