// js assignment

// js assignment operators
// assignment operators are used to assign values to variables

// operator  example  same as
// =         x = y    x = y
// +=        x += y   x = x + y
// -=        x -= y   x = x - y
// *=        x *= y   x = x * y
// /=        x /= y   x = x / y
// %=        x %= y   x = x % y
// **=       x **= y  x = x ** y

// shift assignment operators

// operator  example  same as
// <<=       x <<= y  x = x << y
// >>=       x >>= y  x = x >> y
// >>>=      x >>>= y x = x >>> y

// bitwise assignment operators

// operator  example  same as
// &=        x &= y   x = x & y
// |=        x |= y   x = x | y
// ^=        x ^= y   x = x ^ y

// logical assignment operators

// operator  example  same as
// &&=       x &&= y  x && (x = y)
// ||=       x ||= y  x || (x = y)
// ??=       x ??= y  x ?? (x = y)

// the = operator
// the simple assignment operator assings a value to a variable

// simple assignment examples
let x = 10;

x = 10 + y;

// the += operator
// the addition assignment operator adds a value to a variable

// addition assignment examples
x = 10;
x += 5;

let text = "hello";
text += " world";

// the -= operator
// the substraction assignment operator subtracts a value from a variable

// substraction assignment examples
x = 10;
x -= 5;

// the *= operator
// the multiplication assignment operator multiplies a variable

// multiplication assignment examples
x = 10;
x *= 5;

// the **= operator
// the exponentiation assignment operator raises a variable to the power of the operand

// exponentiation assignment examples
x = 10;
x **= 5;

// the /= operator
// the division assignment operator divides a variable

// division assignment examples
x = 10;
x /= 5;

// the %= operator
// the remainder assignment operator assigns a remainder to a variable

// remainder assignment examples
x = 10;
x %= 5;

// the <<= operator
// the left shift assignment operator shifts a variable to the left

// left shift assignment examples
x = 10;
x <<= 5;

// the >>= operator
// the right shift assignment operator shifts a variable to the right

// right shift assignment examples
x = 10;
x >>= 5;

// the >>>= operator
// the unsigned right shift assignment operator right shifts a variable (unsigned)

// unsigned right shift assignment examples
x = 10;
x >>>= 5;

// the &= operator
// the bitwise AND assignment operator does a bitwise AND operation on two operands and
// assigns the result to the variable

// bitwise AND assignment examples
x = 10;
x &= 5;

// the |= operator
// the bitwise OR assignment operator does a bitwise OR operation on two operands and
// assigns the result to the variable

// bitwise OR assignment examples
x = 10;
x |= 5;

// the ^= operator
// the bitwise XOR assignment operator does a bitwise XOR operation on two operands and
// assigns the result to the variable

// bitwise XOR assignment examples
x = 10;
x ^= 5;

// the &&= operator
// the logical AND assignment is used between two values
// if the first value is true, the second value is assigned

// logical AND assignment examples
x = 10;
x &&= 5;

// the ||= operator
// the logical OR assignment is used between two values
// if the first value is false, the second value is assigned

// logical OR assignment examples
x = 10;
x ||= 5;

// the ??= operator
// the nullish coalescing assignment operator is used between two values
// if the first value is null or undefined, the second value is assigned

// nullish coalescing assignment examples
x = 10;
x ??= 5;