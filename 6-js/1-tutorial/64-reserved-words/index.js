// js reserved words

// in js you cannot use these reserved words as variables, labels, or function names

// abstract     arguments   await*      boolean
// break        byte        case        catch
// char         class*      const       continue
// debugger     default     delete      do
// double       else        enum*       eval
// export*      extends*    false       final
// finally      float       for         function
// goto         if          implements  import*
// in           instanceof  int         interface
// let*         long        native      new
// null         package     private     protected
// public       return      short       static
// super*       switch      synchronized this
// throw        throws      transient   true
// try          typeof      var         void
// volatile     while       with        yield

// words marked with* are new in ecmascript 6 (2015)

// removed reserved words
// the following reserved words have been removed from the ecmascript 5 (2009) 
// standard:

// abstract   boolean     byte        char
// double     final       float       goto
// int        long        native      short
// synchronized   transient   volatile

// do not use these words as variable. es5/6 does not have full support in all browsers

// js objects, properties, and methods
// you should also avoid using the name of js built-in objects, properties, and methods

